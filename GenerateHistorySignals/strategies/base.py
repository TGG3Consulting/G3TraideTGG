from abc import ABC, abstractmethod
from dataclasses import dataclass, field
from datetime import datetime
from typing import List, Dict, Any, Optional


@dataclass
class Signal:
    """Trading signal generated by a strategy."""
    date: datetime
    symbol: str
    direction: str  # "LONG" or "SHORT"
    entry: float
    stop_loss: float
    take_profit: float
    reason: str
    metadata: Dict[str, Any] = field(default_factory=dict)
    # Chain/Dedup fields (filled by chain_processor)
    signal_id: str = ""           # 20240115_BTCUSDT_LONG
    chain_id: str = ""            # BTCUSDT_LONG_C001
    chain_seq: int = 0            # Sequence within chain (1, 2, 3...)
    chain_total: int = 0          # Total signals in this chain
    chain_gap_days: int = 0       # Days since previous signal
    is_chain_first: bool = False  # First signal in chain
    is_chain_last: bool = False   # Last signal in chain


@dataclass
class DailyCandle:
    """Daily OHLCV candle with all Binance kline data."""
    date: datetime
    open: float
    high: float
    low: float
    close: float
    volume: float
    quote_volume: float = 0.0           # USDT volume for liquidity checks
    trades_count: int = 0               # Number of trades
    taker_buy_volume: float = 0.0       # Taker buy volume (base asset)
    taker_buy_quote_volume: float = 0.0 # Taker buy volume (USDT)


@dataclass
class StrategyData:
    """
    All data needed for strategy signal generation.
    Passed to strategy's generate_signals() method.
    """
    symbol: str
    candles: List[DailyCandle]
    oi_history: List[Dict]      # Daily Open Interest
    ls_history: List[Dict]      # Daily Long/Short Ratio
    funding_history: List[Dict] # 8h Funding Rate


@dataclass
class StrategyConfig:
    """
    Strategy configuration parameters.
    Each strategy can define its own required parameters.
    """
    sl_pct: float = 5.0         # Stop Loss %
    tp_pct: float = 10.0        # Take Profit %
    max_hold_days: int = 14     # Max hold period
    lookback: int = 7           # Lookback period for calculations

    # Volume filter parameters (Доработка #2)
    order_size_usd: float = 100.0     # Order size in USDT
    min_volume_ratio: float = 0.001   # Max % of daily volume (0.1% = order < 0.1% of volume)

    # Strategy-specific parameters (optional)
    params: Dict[str, Any] = field(default_factory=dict)

    def get(self, key: str, default: Any = None) -> Any:
        """Get a parameter from params dict."""
        return self.params.get(key, default)


class BaseStrategy(ABC):
    """
    Abstract base class for all trading strategies.

    To create a new strategy:
    1. Inherit from BaseStrategy
    2. Set `name` and `description` class attributes
    3. Implement `generate_signals()` method
    4. Optionally override `default_config()` for strategy-specific defaults
    """

    name: str = "base"
    description: str = "Base strategy (abstract)"

    def __init__(self, config: Optional[StrategyConfig] = None):
        """Initialize strategy with configuration."""
        self.config = config or self.default_config()

    @classmethod
    def default_config(cls) -> StrategyConfig:
        """
        Return default configuration for this strategy.
        Override in subclasses for strategy-specific defaults.
        """
        return StrategyConfig()

    @abstractmethod
    def generate_signals(self, data: StrategyData) -> List[Signal]:
        """
        Generate trading signals from data.

        Args:
            data: StrategyData containing candles, OI, L/S, funding history

        Returns:
            List of Signal objects
        """
        pass

    # =========================================================================
    # !!! КРИТИЧЕСКИЕ ВСПОМОГАТЕЛЬНЫЕ МЕТОДЫ - НЕ ИЗМЕНЯТЬ !!!
    # =========================================================================
    # Эти методы используются ВСЕМИ стратегиями для получения данных.
    # Изменение логики здесь СЛОМАЕТ ВСЕ стратегии!
    # Последняя проверка: 2025-03-06
