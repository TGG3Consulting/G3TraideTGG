def main():
    print(f"Loading: {INPUT_FILE}")
    wb = openpyxl.load_workbook(INPUT_FILE)

    ws = wb["Backtest Results"]
    print(f"Adding comments to {len(COLUMN_COMMENTS)} columns...")

    for col, comment_text in COLUMN_COMMENTS.items():
        cell = ws.cell(row=1, column=col)
        # Create comment with author
        comment = Comment(comment_text, "BinanceFriend Backtester")
        comment.width = 300
        comment.height = 50
        cell.comment = comment

    # Also add comments to Analysis sheet
    ws_analysis = wb["Analysis"]

    # Table 1 headers
    analysis_comments_t1 = {
        (2, 1): "Торговая пара",
        (2, 2): "Суммарный чистый PnL по всем сделкам этой монеты",
        (2, 3): "Количество сделок по этой монете",
        (2, 4): "Средний PnL на одну сделку",
        (2, 5): "Процент прибыльных сделок",
    }

    for (row, col), text in analysis_comments_t1.items():
        cell = ws_analysis.cell(row=row, column=col)
        comment = Comment(text, "BinanceFriend Backtester")
        comment.width = 250
        comment.height = 40
        cell.comment = comment

    # Table 2 headers (row 10)
    analysis_comments_t2 = {
        (10, 1): "Период в формате ГГГГ-ММ",
        (10, 2): "Суммарный чистый PnL за этот период",
        (10, 3): "Количество сделок в этом периоде",
        (10, 4): "Монета с лучшим результатом в этом периоде",
        (10, 5): "PnL лучшей монеты в этом периоде",
        (10, 6): "Монета с худшим результатом в этом периоде",
        (10, 7): "PnL худшей монеты в этом периоде",
    }

    for (row, col), text in analysis_comments_t2.items():
        cell = ws_analysis.cell(row=row, column=col)
        comment = Comment(text, "BinanceFriend Backtester")
        comment.width = 250
        comment.height = 40
        cell.comment = comment

    print(f"Saving to: {OUTPUT_FILE}")
    wb.save(OUTPUT_FILE)
    print("Done! Comments added to all headers.")

if __name__ == "__main__":
    main()
